//画线GUI函数
//x1,y1:起点坐标
//x2,y2:终点坐标
//线由点够成，所以incx和incy单位增加正负1
//distance:线段的长度。
void GUI_Drawline(u16 x1,u16 y1,u16 x2,u16 y2,u16 color)
{
	u16 i;
	int xerr=0,yerr=0,delta_x,delta_y,distance;
	int incx,incy,row,col;
	delta_x=x2-x1;//计算坐标增量
	delta_y=y2-y1;
	row=x1;
	col=y1;
	if(delta_x>0)
		incx=1;
	else 
		if(delta_x==0)//画垂直线
			incx=0;
		else
			{
				incx=-1;
				delta_x=-delta_x;
			}
	if(delta_y>0)
		incy=1;
	else 
		if(delta_y==0)//画水平线
			
		{incy=0;}
		else
			{
				incy=-1;
				delta_y=-delta_y;
			}		
	if(delta_x>delta_y)
	{
		distance=delta_x;
	}
	else
		{
			distance=delta_y;
		}
	for(i=0;i<=distance+1;i++)
	{
		GUI_Drawpoint(row,col,color);//画点GUI函数
		xerr+=delta_x;
		yerr+=delta_y;
		if(xerr>distance)
		{
			xerr-=distance;
			row+=incx;
		}
		if(yerr>distance)
		{
			yerr-=distance;
			col+=incy;
		}
	}
}
