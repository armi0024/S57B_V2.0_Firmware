void TFT_Showword(u16 x,u16 y,u8 *num,u8 mode,u16 wordcolor,u16 blackcolor)
{
	u8 t,t1,t2;
	u8 temp;
	u16 y0=y;
//	t=*num;
	if(!mode)
	{
		for(t=0;t<50;t++)	//搜索创建的的字库，t为字库的大小
		{
			if((*num==word[t].Index[0])&&(*(num+1)==word[t].Index[1]))//通过指针*num与所要的字进行对比！
			{
				for(t1=0;t1<32;t1++)		//汉字的取模编码t1
				{
					temp=word[t].Msk[t1];
					for(t2=0;t2<8;t2++)	//扫描8次
					{
						if(temp&0x80)			//字和字母符号都是由一个个像素点构成扫描次数与字模有关
							GUI_Drawpoint(x,y,wordcolor);
						else
							GUI_Drawpoint(x,y,blackcolor);
						temp<<=1;
						y++	;				//字或字符的高度增加
						if(x>=239) {return ;}		//超过x轴的范围就跳出
						if((y-y0)==16)		//对于16*16字体,扫描两次将高度扫完
						{
							y=y0;
							x++;			//每扫描完两个字节后x加1,32个字节，x加16
							if(x>=239) return;	
							break;				//超出区域就退出GUI函数
						}
					}
				}//写完一个字
			}
		}	
	}
}
//显示字符串GUI函数
//(X,Y)起点坐标
//width height为所显示字符的范围,
//以坐标（x,y）为起点,字母和符号，x方向占一个字节一般设置为8的整数倍数为最好,即width=Nx8
//									y方向占两个字节，hight只要在1~16范围之内，均可以显示出16的高度
//wordsize为字体大小,就是字符的长度
//*p：字符串起始地址
//Wordcolor：字的颜色
//blackcolor：背景颜色
void TFT_ShowString(u16 x,u16 y,u16 width,u16 height,u8 wordsize,u8 *p,u16 wordcolor,u16 blackcolor,u8 select)
{
	u8 x0=x;
	width+=x;			//窗口设置，以x 为起点
	height+=y;          //窗口设置，以y 为起点
	while((*p<='~')&&(*p>=' '))//判断是否为非法字符
	{
		if(x>=width)
		{
			x=x0;
			if(wordsize==16)
			{
				y+=wordsize;
			}
			else 
			{ 
				y+=wordsize/3;
			}
		}
		if(y>=height) 
			break;
		switch(select)
		{
			case(0):TFT_Showchar(x,y,*p,wordsize,0,wordcolor,blackcolor);break;//写字符串大小为16*8
			
			case(1):TFT_Showchar1624(x,y,*p,wordsize,0,wordcolor,blackcolor);break;//写字符串大小为16*24
			
			case(2):TFT_Showword(x,y,p,0,wordcolor,blackcolor);break;		//写汉字大小为16*16
			
		}
		x+=wordsize/2;		//此处可以改字符间距
		p++;				//指针变量加一
	}
}
//显示一串汉字GUI函数
//(X,Y)起点坐标
//width height为所显示字符的范围,
//以坐标（x,y）为起点,字母和符号，x方向占一个字节一般设置为8的整数倍数为最好,即width=Nx8
//									y方向占两个字节，hight只要在1~16范围之内，均可以显示出16的高度
//wordsize为字体大小,就是字符的长度
//*p：字起始地址
//Wordcolor：字的颜色
//blackcolor：背景颜色
void TFT_Writeword(u16 x,u16 y,u16 width,u16 height,u8 wordsize,u8 *p,u16 wordcolor,u16 blackcolor)
{
	u8 x0=x;
	width+=x;
	height+=y;
	while(*p!='\0')//判断是否为非法字符
	{
		if(x>=width)
		{
			x=x0;
			y+=wordsize;	//一般跟着字体大小变,改变行距
		}
		if(y>=height) 
			break;	
		
		TFT_Showword(x,y,p,0,wordcolor,blackcolor);		//取指针变量名P，将其传递给指针num
		x+=wordsize/2;		//此处可以改字符间距
		p++;				//指针加一
	}
}

