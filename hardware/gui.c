//清屏并涂上颜色。
//TFT_XMAX:彩屏X轴最大值
//TFT_YMAX:彩屏Y轴最大值
void TFT_ClearScreen(u16 color)
{
	u16 i,j;
	
	TFT_SetWindow(0,0,TFT_XMAX,TFT_YMAX);	 //作用区域 调用窗口设置函数
	for(i=0; i<TFT_XMAX+1; i++)
	{
		for (j=0; j<TFT_YMAX+1; j++)
		{
            write_date_TFTLCD(color);
		}
	}
}
